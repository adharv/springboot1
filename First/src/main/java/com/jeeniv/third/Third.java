package com.jeeniv.third;

public class Third {
private String name;
private String id;
private String phoneno;

public Third() {
}


public Third(String name, String id, String phoneno) {
	super();
	this.name = name;
	this.id = id;
	this.phoneno = phoneno;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getPhoneno() {
	return phoneno;
}
public void setPhoneno(String phoneno) {
	this.phoneno = phoneno;
}


}
